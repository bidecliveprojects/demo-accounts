    <script>
        const ROUTES = {
            todaySales: "{{ route('pos.today-sales') }}",
            lastMonthSales: "{{ route('pos.last-month-sales') }}"
        };

        function loadSalesData(period) {
            console.log('Loading sales data for ' + period);
            let url = period === 'today' ? ROUTES.todaySales : ROUTES.lastMonthSales;

            $.ajax({
                url: url,
                method: 'GET',
                success: function(response) {
                    if (response.success) {
                        let content = `<h4>${response.period} Sales</h4>
                             <p>Total Orders: ${response.order_count}</p>
                             <p>Total Sales: Rs. ${parseFloat(response.total_sales).toLocaleString()}</p>`;
                        $('#salesModalBody').html(content);
                        $('#salesModalTitle').text(`${response.period} Sales Details`);
                        $('#salesModal').modal('show');
                    } else {
                        $('#salesModalBody').html('Error fetching data. first one');
                        $('#salesModal').modal('show');
                    }
                },
                error: function() {
                    $('#salesModalBody').html('Error fetching data.');
                    $('#salesModal').modal('show');
                }
            });
        }
        $(document).ready(function() {

            let cart = [];

            // Update Cart
            function updateCart() {
                let cartTable = document.getElementById("cart-items");
                cartTable.innerHTML = "";

                let totalAmount = 0; // Initialize total amount to 0

                cart.forEach((item, index) => {
                    // Calculate the price of the item (price * quantity)
                    let itemTotal = item.price * item.qty;
                    totalAmount += itemTotal; // Add it to the total amount

                    cartTable.innerHTML += `
                        <tr>
                            <td>
                                <button class="remove-btn" onclick="removeItem(${index})">ðŸ—‘</button> 
                                ${item.name}
                            </td>
                            <td class="text-center">
                                <button class="btn btn-sm btn-secondary qty-btn" onclick="updateQty(${index}, -1)">-</button>
                                ${item.qty}
                                <button class="btn btn-sm btn-secondary qty-btn" onclick="updateQty(${index}, 1)">+</button>
                            </td>
                            <td class="text-end">$${itemTotal.toLocaleString()}</td>
                        </tr>
                    `;
                });

                // Update the total amount in the UI  
                $('#total-amount').text(totalAmount.toFixed(2)); // Set the total amount (formatted to two decimals)
            }

            // Add to cart
            $(document).on('click', '.add-to-cart', function() {
                let product = {
                    id: $(this).data('product-id'),
                    variantId: $(this).data('variant-id'),
                    name: $(this).data('name'),
                    price: parseFloat($(this).data('price')),
                    barcode: $(this).data('barcode'),
                    qty: 1
                };
                let exists = cart.find(p => p.id === product.id && p.variantId === product.variantId);
                if (exists) exists.qty++;
                else cart.push(product);

                updateCart();
            });

            // Barcode scanner input
            $('#barcode-scanner').on('input', function() {
                let barcode = $(this).val().trim();
                let product = $('.add-to-cart').filter(function() {
                    return $(this).data('barcode') === barcode;
                }).first();
                if (product.length) product.click();
                $(this).val('');
            });

            // Update Quantity
            window.updateQty = function(index, change) {
                if (cart[index].qty + change > 0) {
                    cart[index].qty += change;
                    updateCart();
                }
            };

            // Remove Item
            window.removeItem = function(index) {
                cart.splice(index, 1);
                updateCart();
            };

            // Calculate Change
            $('#calculate-change').on('click', function() {
                let total = parseFloat($('#total-amount').text());
                let payment = parseFloat($('#payment-amount').val());
                let change = payment - total;
                $('#change-amount').text(change >= 0 ? change.toFixed(2) : 'Insufficient Amount');
            });

            // Save Transaction
            $('#save-transaction').on('click', function() {
                let total = parseFloat($('#total-amount').text());
                let paymentAmount = parseFloat($('#payment-amount').val());
                let paymentAccount = parseFloat($('#paymentAccount').val());
                let changeAmount = parseFloat($('#change-amount').text());

                if (isNaN(paymentAmount) || paymentAmount < total) {
                    alert("Insufficient payment amount.");
                    return;
                }

                let cartItems = cart.map(item => ({
                    productId: item.id,
                    variantId: item.variantId,
                    name: item.name,
                    price: item.price,
                    qty: item.qty,
                    discount: item.discount || 0
                }));

                let data = {
                    customerId: $('#customer_id').val(),
                    paymentType: $('#payment_type').val(),
                    orderDate: $('#order_date').val(),
                    productType: $('#product-type').val(),
                    cart: cartItems,
                    totalAmount: total,
                    paymentAmount: paymentAmount,
                    paymentAccount: paymentAccount,
                    changeAmount: changeAmount
                };

                // Save to server
                $.ajax({
                    url: "{{ route('pos.store') }}",
                    method: "POST",
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    data: JSON.stringify(data),
                    success: function(response) {
                        if (response.success) {
                            alert('Transaction Saved!');
                            // Reset form and cart
                            $('#customer_id').val('');
                            $('#payment_type').val('');
                            $('#payment-amount').val('');
                            $('#total-amount').text('0.00');
                            $('#change-amount').text('0.00');
                            cart = [];
                            updateCart();
                        } else {
                            alert('Error saving transaction: ' + (response.message ||
                                'Please try again.'));
                        }
                    },
                    error: function() {
                        alert('There was an error saving the transaction.');
                    }
                });
            });

            // Load accounts based on payment type
            function loadAccountsDependPaymentType() {
                var paymentType = $('#payment_type').val();
                var baseUrl = $("#url").val();
                $('#loadAccountsDependPaymentType').html('');
                $.ajax({
                    url: "" + baseUrl + "/pos/loadAccountsDependPaymentType",
                    method: "GET",
                    data: {
                        paymentType: paymentType,
                    },
                    error: function() {
                        alert("error");
                    },
                    success: function(response) {
                        $('#loadAccountsDependPaymentType').html(response);
                    }
                });
            }

            loadAccountsDependPaymentType();

            // Category filter functionality

            let selectedCategories = [];

            $(".category-btn").on("click", function() {
                let categoryId = $(this).data("id");

                // Toggle selection
                if ($(this).hasClass("active")) {
                    $(this).removeClass("active");
                    selectedCategories = selectedCategories.filter(id => id !== categoryId);
                } else {
                    $(this).addClass("active");
                    selectedCategories.push(categoryId);
                }

                // Send the updated selected categories list via AJAX
                $.ajax({
                    url: "{{ route('pos.filterProducts') }}",
                    method: "POST",
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    data: {
                        categories: selectedCategories
                    },
                    success: function(response) {
                        $('#product-container').html(response);
                    },
                    error: function(xhr) {
                        console.error("Error fetching products", xhr);
                    }
                });
            });
        });
    </script>
